									<h4>BATS</h4>
									<p>Spring Bats<br> 
									When warm temperatures start and bugs begin to fly, out come the bats. Spring is a great time to exclude the 
									bat colony from you attic. Juvenile bat pups are born in the last few weeks of May to the first weeks of June, 
									and it is very important that the bat exclusion is completed before this point.</p>

									<p>Summer Bats<br>
									During summer months bats are most active due to warm temperatures and large insect populations. The juvenile 
									pups are learning to fly and are generally ready by the last weeks of July to the first weeks of August. If it 
									is determined that you have a maternal colony with pups, we will have to wait until August to perform your 
									exclusion. If it is male colony, the exclusion can be done immediately. Bats are a protected mammal and very 
									important to our environment, they are always safely relocated.</p>

									<p>Fall Bats<br>
									During fall months, all juvenile bats are flying making this a great time for maternal exclusions. Fall 
									temperatures do fluctuate, but bats will fly until the the frost hits and diminishes their insect food source. 
									It is good to keep the one-way bat doors in tact until late spring to ensure that bats, who may be hibernating 
									in the insulation of your attic during fall and winter, are actually excluded.</p>

									<p>Winter Bats<br>
									Winter can be the best time to start the exclusion process. If your roof is clear and dry, we can perform 
									all necessary repairs. The one-way doors will stay up until late spring to make sure dormant bats have left. 
									Bats have to stay in temperatures above 40 degrees to survive, so you may hear scratching in walls and 
									ceilings during these cold months as they try and stay warm. Bats may end up in basement or living quarters 
									as they network deeper into insulation to survive.</p>

									<p>Rabies<br>
									Rabies is a preventable viral disease of mammals most often transmitted through the bite of a rabid animal. 
									The vast majority of rabies cases reported to the Centers for Disease Control each year occur in wild animals 
									such as; raccoon, skunks, bats and foxes. The rabies virus infects the central nervous system, ultimately 
									causing disease in the brain and death. The principle rabies hosts today are wild carnivores and bats. The 
									number of rabies related human deaths has declined greatly and there are very few every year. Our technicians 
									are equipped with tools to safely remove the bats from your home, you should not attempt to remove a bat 
									yourself as it is not known whether or not they are rabid. If you suspect you have been bitten, you should 
									seek immediate medical care.</p>

									<p>Histoplasmosis<br>
									Histoplasmosis is an infection caused by breathing in spores of a fungus often found in bird and bat 
									droppings. It is most commonly transmitted when these spores become airborne. Some symptoms are; fever, 
									chills, headache, muscle aches, dry cough and chest discomfort. Effective treatments are available for even 
									the most severe forms of Histoplasmosis. A full attic clean-up or restoration is key in removing these spores 
									from your home.</p>

									<p>Bat Bugs<br>
									These are blood-sucking insect parasites that feed primarily on the blood of bats. Bat bugs are closely 
									related to bed bugs, in fact they are so similar that they are often mistaken for bed bugs. Microscopic 
									examination is needed to distinguish them. Bat bugs will bite humans if given the opportunity, making it 
									very important to get an exclusion if you have bats in your home to eliminate the primary hosts.</p>
